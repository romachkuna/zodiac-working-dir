class OfflineZodiacData {
  String logoPicture;
  String name;
  String dateInterval;
  String assetPicture;
  int index;
  String sunSign;
  String ascendant;
  String element;
  String moonSign;

  OfflineZodiacData({
    required this.logoPicture,
    required this.name,
    required this.dateInterval,
    required this.assetPicture,
    required this.index,
    required this.ascendant,
    required this.element,
    required this.moonSign,
    required this.sunSign,
  });
}

class Aries extends OfflineZodiacData {
  Aries(
      {required super.name,
