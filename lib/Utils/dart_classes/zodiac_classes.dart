class OfflineZodiacData {
  String name;
  String dateInterval;
  String assetPicture;
  int index;
  String sunSign;
  String ascendant;
  String element;
  String moonSign;

  OfflineZodiacData({
    required this.name,
    required this.dateInterval,
    required this.assetPicture,
    required this.index,
    required this.ascendant,
    required this.element,
    required this.moonSign,
    required this.sunSign,
  });
}

